public class DoMath {

    public double add (double a, double b){
        double added = a + b;
        return added;

    }

    public double subtract (double a, double b){
        double subtracted = a - b;
        return subtracted;
    }

    public double multiply(double a, double b){
        double multiplied = a * b;
        return multiplied;
    }

    public double divide (double a, double b){
        double divided = a / b;
        return divided;
    }
}
